const name = 'ahmed';

// concatination عملية اضافة نص على اخر
console.log('Welcome to our service ' + 'ahmed');

// interpolation عملية اضافة و ترجمة متغيرات الى نصوص 
console.log('Welcome to our service ' + name + ' you are welcomed anytime');

// backticks for interpolation in ES6
console.log(`Welcome to our service ${name} you are welcomed anytime`);

